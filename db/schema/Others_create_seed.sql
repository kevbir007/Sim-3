CREATE TABLE Other_Users (
    ID SERIAL PRIMARY KEY,
    First_Name varchar(255),
    Last_Name varchar(255),
    Gender varchar(255),
    Hair_Color varchar(255),
    Eye_Color varchar(255),
    Hobby varchar(255),
    Birth_Day varchar(255),
    Birth_Month varchar(255),
    Birth_Year varchar(255),
    MyImage varchar(255)
);